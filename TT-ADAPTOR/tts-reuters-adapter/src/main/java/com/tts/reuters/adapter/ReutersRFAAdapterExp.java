package com.tts.reuters.adapter;

public class ReutersRFAAdapterExp extends Exception {
	private static final long serialVersionUID = 2100161904070088760L;
	
	public ReutersRFAAdapterExp()	{
		super();
	}
	
	public ReutersRFAAdapterExp(String message)	{
		super(message);
	}
	
	public ReutersRFAAdapterExp(String message, Throwable cause)	{
		super(message, cause);
	}
	
	public ReutersRFAAdapterExp(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)	{
		super(message, cause, enableSuppression, writableStackTrace);
	}
	
	public ReutersRFAAdapterExp(Throwable cause)	{
		super(cause);
	}
}